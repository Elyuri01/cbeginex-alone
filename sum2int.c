#include <stdio.h>

 // A program of sum of two integers
 
int main() {
    int a  = 5;              // declare and initialize first integer
    int b  = 8;             // declare and intialize second integer
    int sum = a + b;       // compute the sum of the two integers
    printf("Sum:%d\n", sum);    // print the result using the %d for integers
    return 0;        //exit the program successfully
} 
